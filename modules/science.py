def answer(question):
    question = question.lower()

    if "gravity" in question:
        return "Gravity is the force that attracts objects toward each other."

    elif "speed of light" in question:
        return "The speed of light in a vacuum is approximately 299,792,458 metres per second."

    elif "photosynthesis" in question:
        return "Photosynthesis is the process by which green plants use sunlight to make food."

    return None
